//将二进制的某一指定的位置进行修改
#include <stdio.h>
int main()
{
    int a = 13;
    //00000000000000000000000000001101
//1.将 0 修改为 1 
    //希望将后面的1101 改为1111
    //需要进行的操作是
    //1.将 1 进行左移一位
    //2.将移动后的数与13进行按位或的操作
    a |= (1<<4);
    printf("%d\n", a);      //29
//2.将 1 修改为 0 
    a &= (~(1<<4));
    printf("%d\n", a);      //13
    return 0;
}